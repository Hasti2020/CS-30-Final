# Color codes using ANSI escape sequences
RED = "\033[91m"
GREEN = "\033[92m"
YELLOW = "\033[93m"
BLUE = "\033[94m"
ENDC = "\033[0m"  # Reset to default

# Print examples
print(RED + "This is red like Gryffindor." + ENDC)
print(GREEN + "This is green like Slytherin." + ENDC)
print(YELLOW + "This is yellow like Hufflepuff." + ENDC)
print(BLUE + "This is blue like Ravenclaw." + ENDC)
